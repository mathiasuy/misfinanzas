    private void cargarDatos(){
        try {
                //busco las categorias con los filtros adecuados para nombre y para filtro
                lista_busqueda_categorias = CCategoria.buscarCategorias("nombre", txtBuscar.getText(),"nombre");
                lista_busqueda_descripciones = CCategoria.buscarCategorias("descripcion", txtBuscar.getText(),"nombre");
                //las fusiono sin repetir elementos
                lista_busqueda_descripciones.stream().filter((c) -> (!lista_busqueda_categorias.contains(c))).forEach((c) -> {
                    lista_busqueda_categorias.add(c);
            });
                //establezco tamaño de tabla
                TBM.setRowCount(lista_busqueda_categorias.size());
                int fila = 0;
                //aplico el modelo
                tabla_categoria.setModel(TBM);
                //Cargo la tabla
                for (Categoria c : lista_busqueda_categorias){
                    tabla_categoria.setValueAt(c.getNombre(), fila, 0);
                    tabla_categoria.setValueAt(c.getDescripcion(), fila, 1);
                    fila++;
                }                    
        } catch (Error e) {
            lblMensaje.setText(e.getMessage());
        } catch (Exception e) {
            lblMensaje.setText("Ha ocurrido un error" + e.getMessage());
        }

    }
    
