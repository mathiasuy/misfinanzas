    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        // TODO add your handling code here:
        try {
            if(txtNombre.getText().trim().compareTo("")!=0){
                int opcion = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar?", ":o", 0);
                if (opcion == 0){                     
                    CCategoria.borrarCategoria(txtNombre.getText());
                    lblMensaje.setText("Baja dada con éxito");
                    cargarDatos();            
                }else{
                    lblMensaje.setText("");
                }
            }else{
                lblMensaje.setText("El nombre no debe estar vacío");
            }
        } catch (Error e) {
            lblMensaje.setText(e.getMessage());
        } catch (Exception e) {
            lblMensaje.setText("Ha ocurrido un error " + e.getMessage());
        }        
    }//GEN-LAST:event_btnBajaActionPerformed

